package com.elitecore.netvertexsm.datamanager.gateway.profile.data;

public class GroovyScriptData {
	private long groovySctiptId;
	private int orderNumber;
	private String scriptName;
	private String argument;
	private Long profileId;
	
 
	public long getGroovySctiptId() {
		return groovySctiptId;
	}
	public void setGroovySctiptId(long groovySctiptId) {
		this.groovySctiptId = groovySctiptId;
	}
	public int getOrderNumber() {
		return orderNumber;
	}
	public void setOrderNumber(int orderNumber) {
		this.orderNumber = orderNumber;
	}
	public String getScriptName() {
		return scriptName;
	}
	public void setScriptName(String scriptName) {
		this.scriptName = scriptName;
	}
	public String getArgument() {
		return argument;
	}
	public void setArgument(String argument) {
		this.argument = argument;
	}
	public Long getProfileId() {
		return profileId;
	}
	public void setProfileId(Long profileId) {
		this.profileId = profileId;
	}
}
